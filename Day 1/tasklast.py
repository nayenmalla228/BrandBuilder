list1=[2,4,6,8,10]

list1.append(2)
print(list1)

list1.append(3)
print(list1)

list1.append(5)
list1.append(7)
print(list1)
 
list1.sort()
print(list1)

list1.remove(2)
print(list1)

list1.insert(3,8)
print(list1)

list1.reverse()
print(list1)

list1.extend([6,6,7])
print(list1)
